#!/usr/bin/env python3
'''
Simple script with a 'to_str' function to convert
a floating number to a string.
'''


def to_str(n: float) -> str:
    '''
    Converts a floating number to its string representation.

    Parameters:
    - n (float): The input number.

    Returns:
    str: The string representation of the input number.
    '''
    return str(n)
